# Contoh: Tipe Data Dictionary di Python

mahasiswa = {"nama": "Budi", "nim": "12345", "jurusan": "Informatika"}
print("Data mahasiswa:", mahasiswa)
print("Nama:", mahasiswa["nama"])
mahasiswa["angkatan"] = 2023
print("Setelah ditambah:", mahasiswa)
mahasiswa["jurusan"] = "Sistem Informasi"
print("Setelah diubah:", mahasiswa)
